package com.ictti.hbc.util;

import java.util.Date;


public class InputInjection {
	private String inputInjectionNames;
	private String inputInjectionLevels;
	private Date inputInjectionTimes;
	public String getInputInjectionNames() {
		return inputInjectionNames;
	}
	public void setInputInjectionNames(String inputInjectionNames) {
		this.inputInjectionNames = inputInjectionNames;
	}
	public String getInputInjectionLevels() {
		return inputInjectionLevels;
	}
	public void setInputInjectionLevels(String inputInjectionLevels) {
		this.inputInjectionLevels = inputInjectionLevels;
	}
	public Date getInputInjectionTimes() {
		return inputInjectionTimes;
	}
	public void setInputInjectionTimes(Date inputInjectionTimes) {
		this.inputInjectionTimes = inputInjectionTimes;
	}	
}
